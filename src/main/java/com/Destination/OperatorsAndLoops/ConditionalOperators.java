package com.Destination.OperatorsAndLoops;

public class ConditionalOperators {
	public static void main(String[] args) {
		int a=100;
		int b=50;
		System.out.println("The value1="+a);
		System.out.println("The value2="+b);
		
		boolean res1=a>b;
		System.out.println("Greater than result:"+res1);
		System.out.println("----------------------");
		
		boolean res2=a<b;
		System.out.println("Lesser than result:"+res2);
		System.out.println("----------------------");
		
		
		boolean res3=a>=b;
		System.out.println("Greater than or equal to result:"+res3);
		System.out.println("----------------------");
		
		
		boolean res4=a<=b;
		System.out.println("Lesser than or equal to result:"+res4);
		System.out.println("----------------------");
		
		
		boolean res5=(a==b);
		System.out.println("Equal to result:"+res5);
		System.out.println("----------------------");
		
		
		boolean res6=(a!=b);
		System.out.println("Not equal toresult:"+res6);
		System.out.println("----------------------");
		
		
	}

}

