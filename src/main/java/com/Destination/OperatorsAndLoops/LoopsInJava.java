package com.Destination.OperatorsAndLoops;

public class LoopsInJava {
	public static void main(String[] args) {
		for(int i=1;i<=10;i++) {
			System.out.println("tommorrow we have class at 10am");
		}
		
		System.out.println("+++++++++++++++++++++++++++++++++++++++++++++++++");
		
		
		int j=1;
		while(j<=10) {
			System.out.println("tommorrow we have class at 10 am");
			j++;
		}
		
		int k=1;
		do{
			System.out.println("tommorrow we have class at 10 am");
			k++;
		}while(k<=10);
	}

}
